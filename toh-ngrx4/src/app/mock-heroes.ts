function sleep(milliseconds) {
  const date = Date.now();
  let currentDate = null;
  do {
    currentDate = Date.now();
  } while (currentDate - date < milliseconds);
}

// simulates latency
export function extractHeroes(latency: number) {
  sleep(latency);
  return HEROES;
}
